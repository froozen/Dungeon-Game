package dungeongame.stats;

public class MonsterStats extends Stats {
	public int atk, mAtk;
	public int def, mDef;

	public int getAtk() {
		return atk;
	}

	public int getMAtk() {
		return mAtk;
	}

	public int getDef() {
		return def;
	}

	public int getMDef() {
		return mDef;
	}

}
